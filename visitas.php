              <h3 class="text-secondary"><b>Política editorial</b></h3>
              <p>El Instituto de Investigaciones Sociales privilegia la publicación de las obras de sus
                investigadores; sin embargo,
                los textos de frontera y las antologías podrán ser de la autoría de académicos no adscritos al
                IIS-UNAM.</p>

              <p>La norma general que guía la política editorial es que los productos por publicar cumplan los
                estándares de calidad
                propios del trabajo académico de alto nivel; esto es, que sean trabajos rigurosos, sustentados
                metodológicamente,
                con un análisis sistemático y una bibliografía adecuada. Con respecto a los textos de divulgación,
                no se establecen
                criterios sobre autoría, extensión o género, dadas las características de esta modalidad.</p>

              <p>Para que una obra sea presentada ante el Comité y pueda llevar el sello editorial del IIS-UNAM es
                necesario que el
                titular de la obra sea un académico adscrito al Instituto y que el CEL participe en el proceso de
                evaluación. El o la
                titular de la obra es el autor o la autora que la escribió o, en su caso, quienes encabezaron el
                trabajo de equipo en
                la coordinación o compilación. El o la titular será el único interlocutor del CEL. Las publicaciones
                podrán contar
                hasta con tres autores, coordinadores o compiladores, según el tipo de publicación presentado.
                Las propuestas de publicación de quienes realizan estancias de investigación en el IIS-UNAM deberán
                cumplir con
                las Normas para la publicación de obras presentadas por académicos o académicas en estancia
                posdoctoral y figuras similares.</p>

              <p>Para la publicación de las obras se establecen dos modalidades: impresa o electrónica. Esta última
                tendrá el forma-
                to ePub y contará con el ISBN de libro electrónico. Para asegurar el acceso abierto a la obra, ambas
                modalidades
                serán difundidas en formato PDF, en el repositorio del Instituto.</p>
